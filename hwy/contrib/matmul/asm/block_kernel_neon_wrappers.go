//go:build !noasm && arm64

// NEON Block Kernel wrappers for ARM64
// C += A^T * B using NEON SIMD for square blocks.
package asm

import "unsafe"

// Generate assembly from C using goat
//go:generate go tool goat ../c/block_kernel_neon_arm64.c -O3 --target arm64

// BlockMulAddNEONF32 computes C += A^T * B for square blocks using NEON.
// aT must be pre-transposed (rows are original A columns).
// b is normal row-major.
// All matrices are blockDim × blockDim.
func BlockMulAddNEONF32(aT, b, c []float32, blockDim int) {
	if blockDim == 0 {
		return
	}
	n := blockDim * blockDim
	if len(aT) < n {
		panic("BlockMulAddNEONF32: aT slice too short")
	}
	if len(b) < n {
		panic("BlockMulAddNEONF32: b slice too short")
	}
	if len(c) < n {
		panic("BlockMulAddNEONF32: c slice too short")
	}
	blockDimVal := int64(blockDim)
	block_muladd_neon_f32(
		unsafe.Pointer(&aT[0]),
		unsafe.Pointer(&b[0]),
		unsafe.Pointer(&c[0]),
		unsafe.Pointer(&blockDimVal),
	)
}

